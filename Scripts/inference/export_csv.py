def read_metadata(path):
    """
    Read metadata file content as raw text.

    Parameters
    ----------
    path : Path or None
        Path to metadata file.

    Returns
    -------
    str
        File content or empty string if not found.
    """

    if path is None:
        return ""

    try:
        return Path(path).read_text(encoding="utf-8")
    except Exception:
        return ""


# =========================
# CSV EXPORT PIPELINE
# =========================

def export_to_csv():
    """
    Converts raw inference JSON into a structured CSV file.

    This function:
    - Loads predictions generated by inference.py
    - Reconstructs original image identity (L/R crops)
    - Associates metadata files with each image
    - Flattens detection results (bbox + confidence)
    - Saves final dataset into a CSV format

    CSV columns:
        image_file, original_image, crop_side,
        metadata_file, metadata_content,
        class, confidence,
        bbox_xmin, bbox_ymin, bbox_xmax, bbox_ymax

    Returns
    -------
    None
    """

    with open(RAW_PRED_PATH, "r", encoding="utf-8") as f:
        data = json.load(f)

    os.makedirs(CSV_OUTPUT_PATH.parent, exist_ok=True)

    with open(CSV_OUTPUT_PATH, "w", newline="", encoding="utf-8") as csv_file:

        writer = csv.writer(csv_file)

        # Header
        writer.writerow([
            "image_file",
            "original_image",
            "crop_side",
            "metadata_file",
            "metadata_content",
            "class",
            "confidence",
            "bbox_xmin",
            "bbox_ymin",
            "bbox_xmax",
            "bbox_ymax",
        ])

        for item in data:

            image_file = item["image"]

            stem = Path(image_file).stem
            original, side = recover_original_name(stem)

            metadata_path = find_metadata_file(INPUT_DIR, original)
            metadata_content = read_metadata(metadata_path)

            for pred in item["predictions"]:

                bbox = pred["bbox"]

                writer.writerow([
                    image_file,
                    original,
                    side,
                    metadata_path.name if metadata_path else "",
                    metadata_content,
                    pred["class"],
                    pred["confidence"],
                    bbox["xmin"],
                    bbox["ymin"],
                    bbox["xmax"],
                    bbox["ymax"],
                ])

    print(f"CSV generated at: {CSV_OUTPUT_PATH}")


# =========================
# ENTRY POINT
# =========================

if __name__ == "__main__":
    export_to_csv()
