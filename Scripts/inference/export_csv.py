import os
import csv
import json
from pathlib import Path


# =========================
# CONFIGURATION
# =========================

INPUT_DIR = r"" # Path to dataset directory containing images and metadata
RAW_PRED_PATH = Path("outputs/predictions_raw.json")
CSV_OUTPUT_PATH = Path("outputs/predictions.csv")


# =========================
# UTILITIES
# =========================

def recover_original_name(file_stem):
    """
    Recover original image name and crop side indicator.

    The inference pipeline processes images split into left/right crops.
    This function restores the original image identity.

    Examples
    --------
    "image_001_L" -> ("image_001", "L")
    "image_001_R" -> ("image_001", "R")

    Parameters
    ----------
    file_stem : str
        Filename without extension.

    Returns
    -------
    tuple
        (original_name, crop_side)
    """
    if file_stem.endswith("_L"):
        return file_stem[:-2], "L"

    if file_stem.endswith("_R"):
        return file_stem[:-2], "R"

    return file_stem, "FULL"


def find_metadata_file(folder, original_name):
    """
    Locate metadata file associated with an image.

    The dataset structure assumes:
    - image file
    - metadata file with same stem
    - optional extra file (ignored)

    Parameters
    ----------
    folder : str or Path
        Dataset directory containing images and metadata.

    original_name : str
        Base name of the image without crop suffix.

    Returns
    -------
    Path or None
        Path to metadata file if found, otherwise None.
    """

